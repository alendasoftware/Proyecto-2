class phpCifrado{
    
    //Propiedades pblicas  
    Public $Texto;	
	
    // *****************************************************************************
    // * Creacin y eliminacin
    // *****************************************************************************
    Public function __construct(){				
		
	   //Crea objetos		

	   // Inicia propiedades        
		
	   $this->Inicia();
    } 	
    
    function __destruct() {
		
	   //Hay que destruir los objetos		        
	
    }

    //  *****************************************************************************
    //  * Inicio
    //  *****************************************************************************
    Public function Inicia(){
		
	   $this->Texto = null;      
		
    }	

    // *****************************************************************************
    // * Propiedades
    // *****************************************************************************  
    

    // *****************************************************************************
    // * Mtodos Pblicos
    // *****************************************************************************
    Public function DevuelveCadena(){
		
	$strValor = "";
		
    	$strValor .= "Texto:" . $this->Texto . ";";
    		
    	return $strValor;

    } 
	
    
    Public function Md5(){
	
    	$strValor = "";
    	if (!($this->Texto==null) && ($this->Texto!="")){
    		$strValor = md5($this->Texto);
    	}	
    	return $strValor;
    }
    
    // *****************************************************************************
    // * Mtodos Privados
    // *****************************************************************************

}

